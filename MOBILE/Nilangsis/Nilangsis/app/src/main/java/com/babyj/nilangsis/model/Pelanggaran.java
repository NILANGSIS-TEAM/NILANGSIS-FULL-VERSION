package com.babyj.nilangsis.model;

public class Pelanggaran {

    String KodeTP, TipePelanggaran, KodeLP;

    public String getKodeTP() {
        return KodeTP;
    }

    public void setKodeTP(String kodeTP) {
        KodeTP = kodeTP;
    }

    public String getTipePelanggaran() {
        return TipePelanggaran;
    }

    public void setTipePelanggaran(String tipePelanggaran) {
        TipePelanggaran = tipePelanggaran;
    }

    public String getKodeLP() {
        return KodeLP;
    }

    public void setKodeLP(String kodeLP) {
        KodeLP = kodeLP;
    }
}
