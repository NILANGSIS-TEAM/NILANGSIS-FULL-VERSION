package com.babyj.nilangsis.model;

public class Siswa {

    private String NIS, NamaSW, JenisKelamin, TempatLahir, TanggalLahir, Kelas, KodeIK, NomorHPSiswa, NomorHPWali, Alamat;

    public String getNIS() {
        return NIS;
    }

    public void setNIS(String NIS) {
        this.NIS = NIS;
    }

    public String getNamaSW() {
        return NamaSW;
    }

    public void setNamaSW(String namaSW) {
        NamaSW = namaSW;
    }

    public String getJenisKelamin() {
        return JenisKelamin;
    }

    public void setJenisKelamin(String jenisKelamin) {
        JenisKelamin = jenisKelamin;
    }

    public String getTempatLahir() {
        return TempatLahir;
    }

    public void setTempatLahir(String tempatLahir) {
        TempatLahir = tempatLahir;
    }

    public String getTanggalLahir() {
        return TanggalLahir;
    }

    public void setTanggalLahir(String tanggalLahir) {
        TanggalLahir = tanggalLahir;
    }

    public String getKelas() {
        return Kelas;
    }

    public void setKelas(String kelas) {
        Kelas = kelas;
    }

    public String getKodeIK() {
        return KodeIK;
    }

    public void setKodeIK(String kodeIK) {
        KodeIK = kodeIK;
    }

    public String getNomorHPSiswa() {
        return NomorHPSiswa;
    }

    public void setNomorHPSiswa(String nomorHPSiswa) {
        NomorHPSiswa = nomorHPSiswa;
    }

    public String getNomorHPWali() {
        return NomorHPWali;
    }

    public void setNomorHPWali(String nomorHPWali) {
        NomorHPWali = nomorHPWali;
    }

    public String getAlamat() {
        return Alamat;
    }

    public void setAlamat(String alamat) {
        Alamat = alamat;
    }
}
