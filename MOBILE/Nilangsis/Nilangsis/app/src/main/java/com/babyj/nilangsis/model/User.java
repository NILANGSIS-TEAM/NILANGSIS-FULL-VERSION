package com.babyj.nilangsis.model;

public class User {

    private String KodeUSR;
    private String NIP;
    private String Username;
    private String Password;
    private String KodeLU;

    public String  getKodeUSR(){
        return KodeUSR;
    }

    public void setKodeUSR(String KodeUSR){
        this.KodeUSR = KodeUSR;
    }

    public String NIP(){
        return NIP;
    }

    public String getNIP(){
        return NIP;
    }

    public void setNIP(String NIP){
        this.NIP = NIP;
    }

    public String getUsername(){
        return Username;
    }

    public void setUsername(String Username){
        this.Username = Username;
    }

    public String getPassword(){
        return Password;
    }

    public void setPassword(String Password){
        this.Password = Password;
    }

    public String  getKodeLU(){
        return KodeLU;
    }

    public void setKodeLU(String KodeLU){
        this.KodeLU = KodeLU;
    }




}
